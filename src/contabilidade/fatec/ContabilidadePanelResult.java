    private void jbtnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMostrarActionPerformed
        // TODO add your handling code here:
        model2.insertRow(model2.getRowCount(), new Object[]{nome[0].toString(), dependentes[0],salario[0],vt[0],inss[0],irrf[0],liquido[0],fgts[0]});
        model2.insertRow(model2.getRowCount(), new Object[]{nome[1].toString(), dependentes[1],salario[1],vt[1],inss[1],irrf[1],liquido[1],fgts[1]});
        model2.insertRow(model2.getRowCount(), new Object[]{nome[2].toString(), dependentes[2],salario[2],vt[2],inss[2],irrf[2],liquido[2],fgts[2]});
        model2.insertRow(model2.getRowCount(), new Object[]{nome[3].toString(), dependentes[3],salario[3],vt[3],inss[3],irrf[3],liquido[3],fgts[3]});
        model2.insertRow(model2.getRowCount(), new Object[]{nome[4].toString(), dependentes[4],salario[4],vt[4],inss[4],irrf[4],liquido[4],fgts[4]});
//      
        CalcularFolha.Arredondar arredonda = new CalcularFolha.Arredondar();       
        totGeral =  arredonda.converterDoisDecimais(inssPatronal +  totINSS +   totIRRF + totFGTS + totLiquido) ;
       
        
       // model.insertRow(model.getRowCount(), new Object[]{"Salario Bruto Total",  totSalBruto });
        model.insertRow(model.getRowCount(), new Object[]{"INSS Patronal",  inssPatronal });
        model.insertRow(model.getRowCount(), new Object[]{"INSS ",  totINSS });
        model.insertRow(model.getRowCount(), new Object[]{"IRRF ",  totIRRF });
        model.insertRow(model.getRowCount(), new Object[]{"FGTS ",  totFGTS });
        model.insertRow(model.getRowCount(), new Object[]{"Salario Liquido ",  totLiquido });
        model.insertRow(model.getRowCount(), new Object[]{"Total ",  totGeral });
        
        
        
        ContabilidadePanelResult.totLiquido = totLiquido;
        
        
        jbtnMostrar.setEnabled(false);
    }//GEN-LAST:event_jbtnMostrarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int qtd = model2.getRowCount();
        
        for (int i = qtd - 1; i >= 0; i--) {
            model2.removeRow(i);
        }
        jbtnMostrar.setEnabled(true);
    }//GEN-LAST:event_jButton3ActionPerformed

